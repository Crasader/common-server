package cn.com.kanjian.model;

import java.io.Serializable;
import java.util.Date;

public class ResourceTags implements Serializable {

	private static final long serialVersionUID = -1995028494586280333L;

	private Long id;
	private Long resid;
	private Long tagid;
	private Date intime;

	public ResourceTags() {
	}

	public ResourceTags(Long resid, Long tagid) {
		super();
		this.resid = resid;
		this.tagid = tagid;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getResid() {
		return resid;
	}

	public void setResid(Long resid) {
		this.resid = resid;
	}

	public Long getTagid() {
		return tagid;
	}

	public void setTagid(Long tagid) {
		this.tagid = tagid;
	}

	public Date getIntime() {
		return intime;
	}

	public void setIntime(Date intime) {
		this.intime = intime;
	}

}
