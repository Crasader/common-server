package cn.com.kanjian.exception;

public class MybatisEntityException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public MybatisEntityException() {
		super();
	}

	public MybatisEntityException(String message, Throwable cause,	boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public MybatisEntityException(String message, Throwable cause) {
		super(message, cause);
	}

	public MybatisEntityException(String message) {
		super(message);
	}

	public MybatisEntityException(Throwable cause) {
		super(cause);
	}

}
